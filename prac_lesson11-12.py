age = int(input("введіть ваш вік"))
day = int(input("введіть день тижня(1-7)"))
price = 0
if 1 < day < 7 and age < 0:
    print("Помилка вводу")
else:
    if day <= 5:
        price += 160
    else:
        price += 200
    if age < 6:
        price = 0
