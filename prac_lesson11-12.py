# Завдання 1: «Комуналка за електроенергію»
# Умова
# Користувач вводить споживання електроенергії за місяць у кВтгод.
# Потрібно порахувати суму до сплати за тарифними діапазонами.
# Вимоги (що зробити)
# Зчитати kwh (ціле число).
# Порахувати оплату по частинах:
# перші 100 кВт·год → 1.20 грн/кВт год
# наступні 200 (тобто 101–300) → 1.50 грн/кВт·год
# усе, що понад 300 → 1.80 грн/кВт год
# Вивести:
# загальну суму
# окремо: скільки кВт·год у кожному діапазоні
# Приклад вводу/виводу
# Ввід (що вводять):
# Введіть споживання (кВт·год): 350
# Вивід (що вивести):
# До 100: 100 кВт·год
# 101-300: 200 кВт·год
# Понад 300: 50 кВт·год
# Сума: 525.0 грн
# Додаткові коментарі (рекомендації)
# Якщо kwh < 0 — вивести Помилка вводу.
# Розв’язання тільки через if / elif / else і арифметику (без циклів, без методів).


kwl = int(input("ведіть споживання (кВт·год):"))

if kwl < 0:
    print("Помилка вводу")
else:
    if kwl <= 100:
        t1 = kwl
        t2 = 0
        t3 = 0
    elif kwl <= 300:
        t1 = 100
        t2 = kwl - 100
        t3 = 0
    else:
        t1 = 100
        t2 = 200
        t3 = kwl - 300

total = t1 * 1.2 + t2 * 1.5 + t3 * 1.8

print(f"До 100: {t1} кВт·год")
print(f"101-300: {t2} кВт·год")
print(f"Понад 300: {t3} кВт·год")
print(f"Сума: {total} грн")

# Завдання 2: «Кінотеатр: ціна квитка»
