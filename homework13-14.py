# Діапазон має автоматично нормалізуватися, якщо початок більший за кінець.
start = int(input("Enter start number: "))
end = int(input("Enter end number: "))
if start > end:
    start, end = end, start
i = start
dobutok = 1
while i <= end:
    if i % 4 == 0 and i % 6 != 0:
        dobutok *= i
    i += 1

if dobutok > 1:
    print(f"добуток усіх чисел {dobutok}")
else:
    print("немає таких чисел")

