end = int(input("Enter end number: "))
interval = int(input("Enter interval: "))
choice = int(input("Виберіть порядок виведення: 1.у прямому або 2.зворотному порядку: "))

if choice == 1:
    i = start
    while i <= end:
        print(i)
        i += interval
elif choice == 2:
    i = end
    while i >= start:
        print(i)
        i -= interval
else:
    print("помилка")
