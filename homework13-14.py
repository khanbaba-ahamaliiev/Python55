# Потрібно проаналізувати всі числа в цьому діапазоні. Виведення на екран
# має відбуватися за правилами, зазначеними нижче.
# Якщо число кратне 3 (ділиться на 3 без залишку) потрібно вивести
# слово "Fizz". Якщо число кратне 5 потрібно вивести слово "Buzz". Якщо число
# кратне 3 і 5 потрібно вивести "Fizz Buzz". Якщо число не кратне не 3 і 5
# потрібно вивести саме число.
start = int(input("Enter start number: "))
end = int(input("Enter end number: "))
i = start
while i <= end:
    if i % 3 == 0 and i % 5 == 0:
        print("Fizz Buzz")
    elif i % 3 == 0:
        print("Fizz")
    elif i % 5 == 0:
        print("Buzz")
    else:
        print(i)
    i += 1
